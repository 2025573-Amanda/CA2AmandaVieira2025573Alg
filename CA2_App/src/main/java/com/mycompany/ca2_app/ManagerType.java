/**
 * Enum representing the different types of managers
 * that an Employee can be assigned to.
 *
 * This is used when creating Employee records
 * and when inserting them into the Binary Tree.
 */
/**
 *
 * @author home
 */
public enum ManagerType {
       // Represents a Junior Manager
    JUNIOR,

    // Represents a Senior Manager
    SENIOR,

    // Represents a Team Lead
    TEAM_LEAD,

    // Represents a Department Manager
    DEPARTMENT_MANAGER
}

